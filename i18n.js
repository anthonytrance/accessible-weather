export const STRINGS = { en, nl, fr, de, es };

export function isSupportedLanguage(code) {
  return Object.prototype.hasOwnProperty.call(STRINGS, code);
}

export function resolveLanguage(preference, navigatorLanguage = "en") {
  if (preference && preference !== "auto" && isSupportedLanguage(preference)) return preference;
  const detected = String(navigatorLanguage || "en").slice(0, 2).toLowerCase();
  return isSupportedLanguage(detected) ? detected : "en";
}

export function translate(lang, key, params = {}) {
  const table = STRINGS[lang] ?? en;
  const template = table[key] ?? en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (match, name) =>
    Object.prototype.hasOwnProperty.call(params, name) ? String(params[name]) : match
  );
}

export function createTranslator(lang) {
  return (key, params) => translate(lang, key, params);
}

export function localizedWeatherLabel(lang, code) {
  const key = `code.${Number(code)}`;
  const table = STRINGS[lang] ?? en;
  return table[key] ?? en[key] ?? translate(lang, "code.unknown");
}

export function localizedCompass(lang, degrees) {
  if (!Number.isFinite(Number(degrees))) return translate(lang, "compass.unknown");
  const index = Math.round(Number(degrees) / 22.5) % 16;
  return translate(lang, `compass.${index}`);
}

export function intensityKey(mmPerHour) {
  if (mmPerHour < 0.05) return "intensity.dry";
  if (mmPerHour < 0.2) return "intensity.veryLight";
  if (mmPerHour < 1) return "intensity.light";
  if (mmPerHour < 3) return "intensity.moderate";
  if (mmPerHour < 8) return "intensity.heavy";
  return "intensity.veryHeavy";
}

// Formats the structured result of summarizeRain() into localized sentences.
// sourceKind is "radar" or "model"; formatTime maps an epoch to a local time string.
export function formatRainSummary(lang, summary, formatTime, sourceKind) {
  const t = createTranslator(lang);
  const source = t(sourceKind === "radar" ? "rain.source.radar" : "rain.source.model");

  if (summary.kind === "unavailable") {
    return { headline: t("rain.unavailable.headline"), detail: t("rain.unavailable.detail", { source }) };
  }
  if (summary.kind === "dry") {
    return { headline: t("rain.none.headline"), detail: t("rain.none.detail", { count: summary.count, source }) };
  }

  const strongest = t(intensityKey(summary.strongestMmPerHour));
  const first = t(intensityKey(summary.firstMmPerHour));
  let headline;
  let detail = t("rain.detail.strongest", { intensity: strongest, time: formatTime(summary.strongestTime) });
  if (summary.kind === "raining") {
    const capitalized = first.charAt(0).toUpperCase() + first.slice(1);
    headline = t("rain.now.headline", { intensity: capitalized, time: formatTime(summary.endTime) });
  } else {
    headline = t("rain.upcoming.headline", { minutes: summary.minutesUntil, time: formatTime(summary.firstTime) });
    detail += ` ${t("rain.detail.end", { time: formatTime(summary.endTime) })}`;
  }
  return { headline, detail };
}

export function uvRatingKey(value) {
  if (value < 3) return "uv.rating.low";
  if (value < 6) return "uv.rating.moderate";
  if (value < 8) return "uv.rating.high";
  if (value < 11) return "uv.rating.veryHigh";
  return "uv.rating.extreme";
}

export function europeanAqiRatingKey(value) {
  if (value <= 20) return "air.rating.good";
  if (value <= 40) return "air.rating.fair";
  if (value <= 60) return "air.rating.moderate";
  if (value <= 80) return "air.rating.poor";
  if (value <= 100) return "air.rating.veryPoor";
  return "air.rating.extremelyPoor";
}

export function usAqiRatingKey(value) {
  if (value <= 50) return "air.rating.good";
  if (value <= 100) return "air.rating.fair";
  if (value <= 150) return "air.rating.moderate";
  if (value <= 200) return "air.rating.poor";
  if (value <= 300) return "air.rating.veryPoor";
  return "air.rating.extremelyPoor";
}

export function beaufortForce(kmh) {
  const thresholds = [1, 6, 12, 20, 29, 39, 50, 62, 75, 89, 103, 118];
  let force = 0;
  for (const limit of thresholds) {
    if (kmh >= limit) force += 1;
  }
  return force;
}
